// Langue par défaut : Anglais
let currentLang = localStorage.getItem('preferred_lang') || 'en';

// Fonction qui applique les traductions
function applyTranslations(lang) {
    const data = translations[lang];
    if (!data) {
        console.error('❌ Langue non trouvée :', lang);
        return;
    }

    console.log(`🟢 Application de la langue : ${lang}`);

    // Chercher tous les éléments avec data-key
    const elements = document.querySelectorAll('[data-key]');
    console.log(`📌 ${elements.length} éléments à traduire`);

    elements.forEach(el => {
        const key = el.dataset.key;
        if (data[key] !== undefined) {
            // Utiliser textContent pour les textes simples, innerHTML pour les tags (comme les spans)
            if (el.innerHTML.includes('<') || el.innerHTML.includes('</')) {
                // Si l'élément contient du HTML, utiliser innerHTML
                el.innerHTML = data[key];
            } else {
                el.textContent = data[key];
            }
        } else {
            console.warn(`⚠️ Clé manquante : "${key}"`);
        }
    });

    // Mettre à jour la langue du document
    document.documentElement.lang = lang;
    localStorage.setItem('preferred_lang', lang);

    // Mettre à jour les boutons actifs
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    console.log(`✅ Traduction appliquée : ${lang}`);
}

// Fonction pour changer de langue
function switchLanguage(lang) {
    if (translations[lang]) {
        applyTranslations(lang);
    } else {
        console.error('❌ Langue inconnue :', lang);
    }
}

// ============================================
// INITIALISATION
// ============================================

// Attendre que le DOM soit complètement chargé
document.addEventListener('DOMContentLoaded', function() {
    console.log('🚀 DOM chargé, application des traductions...');
    applyTranslations(currentLang);

    // Ajouter les écouteurs sur les boutons de langue
    const langButtons = document.querySelectorAll('.lang-btn');
    console.log(`🔘 ${langButtons.length} boutons de langue trouvés`);

    langButtons.forEach(btn => {
        btn.addEventListener('click', function(e) {
            const lang = this.dataset.lang;
            console.log(`🔄 Clic sur le bouton : ${lang}`);
            switchLanguage(lang);
        });
    });
});

// Rendre les fonctions disponibles globalement pour déboguer dans la console
window.applyTranslations = applyTranslations;
window.switchLanguage = switchLanguage;
window.translations = translations;